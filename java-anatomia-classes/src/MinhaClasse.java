public class MinhaClasse {
    public static void main (String [] args) {
// A classe, quando é executável e tem a capacidade de inicializar o projeto (ou de forma independente), ela precisa de um método específico, chamado main.

        System.out.print("Hello World!");

/*
Aqui, dizemos:
O sistema vai realizar uma ação de saída, utilizando uma exibição de "Hello World!"."
*/

        final String BR = "Brasil";
        double PI = 3.14;
        int ESTADOS_BRASILEIRO = 27;
        int ANO_2000 = 2000;

/* 
Nesse caso, declaramos como "final" a variável do tipo string BR. A letra maiúscula é uma forma de indicar que a variável é imutável.
Outros exemplos estão abaixo.
 */
    }
}
